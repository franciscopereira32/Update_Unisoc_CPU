    private void open_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_open_btnActionPerformed
        // TODO add your handling code here:
        try{
            SerialPort []portLists = SerialPort.getCommPorts();
            serialPort1 = portLists[jComboBox1_com.getSelectedIndex()];
            //serialPort1.setNumData(Integer.parseInt(jComboBox2_data.getSelectedItem().toString()));
            //serialPort1.setStopBits(Integer.parseInt(jComboBox3_stop.getSelectedItem().toString()));
            if(serialPort1.isOpen()){//It only works if the user is going to write with serial communication with the PCB
                JOptionPane.showMessageDialog(this, serialPort1.getDescriptivePortName() + " -- Sucesso!");
                jComboBox1_com.setEnabled(true);
                jProgressBar1.setValue(100);
            }
            else{
                JOptionPane.showMessageDialog(this, serialPort1.getDescriptivePortName() + " -- Sucesso!");
                jProgressBar1.setValue(100);
                enviar_btn.setEnabled(true);
                close_btn.setEnabled(true);
            }
        }
        catch(ArrayIndexOutOfBoundsException a){
            JOptionPane.showMessageDialog(this, "SELECIONE PORTA COM!", "ERRO", ERROR_MESSAGE);
            
        }
        catch(Exception b){
            JOptionPane.showMessageDialog(this, b, "ERRO", ERROR_MESSAGE);
        }
    }//GEN-LAST:event_open_btnActionPerformed

